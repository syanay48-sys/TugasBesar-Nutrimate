    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        try {
        // 1. Mengambil data angka yang diketik user di form input
        double berat = Double.parseDouble(txtBerat.getText().trim());
        double tinggi = Double.parseDouble(txtTinggi.getText().trim());
        int umur = Integer.parseInt(txtUmur.getText().trim());
        String jk = cbJenisKelamin.getSelectedItem().toString();
        
        double bmr = 0;
        
        // 2. Rumus kalkulator kalori (Harris-Benedict)
        if (jk.equals("Laki-laki")) {
            bmr = 66.5 + (13.75 * berat) + (5.003 * tinggi) - (6.75 * umur);
        } else {
            bmr = 655.1 + (9.563 * berat) + (1.850 * tinggi) - (4.676 * umur);
        }
        
        // 3. Memasukkan hasil hitungan ke label visual
        lblHasilKalori.setText("Kebutuhan Kalori: " + Math.round(bmr) + " Kcal");
        
    } catch (NumberFormatException e) {
        // Jika user mengosongkan input atau mengetik huruf/tulisan bukan angka
        javax.swing.JOptionPane.showMessageDialog(this, 
                "Mohon masukkan data angka yang valid!", 
                "Input Salah", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
    }

    }//GEN-LAST:event_btnHitungActionPerformed

