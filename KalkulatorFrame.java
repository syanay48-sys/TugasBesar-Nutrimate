    public void tampilkanTabelMakanan() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Makanan");
    model.addColumn("Nama Makanan");
    model.addColumn("Kalori (Kcal)");
    model.addColumn("Protein (g)");
    model.addColumn("Karbohidrat (g)");
    model.addColumn("Lemak (g)");
    
    try {
        MakananDAO dao = new MakananDAO();
        List<Makanan> daftarMakanan = dao.getAllMakanan(); 
        
        for (Makanan m : daftarMakanan) {
            model.addRow(new Object[]{
                m.getIdMakanan(),
                m.getNamaMakanan(),
                m.getKalori(),
                m.getProtein(),
                m.getKarbohidrat(),
                m.getLemak()
            });
        }
        
        // MASUKKAN KE SINI: Set model dipanggil TEPAT setelah data selesai di-looping
        tblMakanan.setModel(model); 
        
    } catch (Exception e) {
        System.out.println("Gagal memuat data ke tabel: " + e.getMessage());
    }
}
